void main(){
  //Sabitler (final ve const)

  //Genelde variablelar ile kullanılır. (Run edilidğinde hafızada yer alır.)
  final int numara = 58;
  print(numara);
  //numara = 200;   final key i varsa değiştiremezsin


  //Genelde Görsel nesneler ile kullanılır. (Değişken tanımlandığında hafızada yer alır.)
  const int numara2 = 580;
  print(numara2);
  //numara = 300;   const key i varsa değiştiremezsin
}